function [T] = imu2table(imuMessage)
%IMU2TABLE Converts an IMU message to a table row.
%   Converts a 'sensor_msgs/Imu' type message from a ROS bag to a table row.
%   Just the orientation, ang. velocity and lin. acceleration are
%   preserved.
    IMUOrientationX = imuMessage.Orientation.X;
    IMUOrientationY = imuMessage.Orientation.Y;
    IMUOrientationZ = imuMessage.Orientation.Z;
    IMUOrientationW = imuMessage.Orientation.W;
    IMUAngularVelocityX = imuMessage.AngularVelocity.X;
    IMUAngularVelocityY = imuMessage.AngularVelocity.Y;
    IMUAngularVelocityZ = imuMessage.AngularVelocity.Z;
    IMULinearAccelerationX = imuMessage.LinearAcceleration.X;
    IMULinearAccelerationY = imuMessage.LinearAcceleration.Y;
    IMULinearAccelerationZ = imuMessage.LinearAcceleration.Z;

    T = table(...
        IMUOrientationX,...
        IMUOrientationY,...
        IMUOrientationZ,...
        IMUOrientationW,...
        IMUAngularVelocityX,...
        IMUAngularVelocityY,...
        IMUAngularVelocityZ,...
        IMULinearAccelerationX,...
        IMULinearAccelerationY,...
        IMULinearAccelerationZ...
    );

end

